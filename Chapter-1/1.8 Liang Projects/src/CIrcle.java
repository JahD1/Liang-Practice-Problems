/*
* Write a program that displays the area and perimeter of a circle that has a radius of 5.5
* using the following formula:
* perimeter = 2 * radius * pi
* area = radius * radius * pi
 */

public class CIrcle {
    public static void main(String[] args)
    {
        System.out.println("Perimeter of a circle with a radius of 5.5 = " + 2 * 5.5 * 3.14159);
        System.out.println("Area of a circle with a radius of 5.5 = " + 5.5 * 5.5 * 3.14159);
    }
}
