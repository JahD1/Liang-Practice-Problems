/*
*Write a program that displays the area and perimeter of a rectangle with a width of
* 4.5 and a height of 7.9 using the following formulas:
* perimeter = 2 * (width + height)
* area = width * height
 */

public class Rectangle {
    public static void main (String[] args)
    {
        System.out.println("Perimeter of the rectangle = " + (2 * (4.5 + 7.9)));
        System.out.println("Area of the rectangle = " + (4.5 * 7.9));
    }
}
